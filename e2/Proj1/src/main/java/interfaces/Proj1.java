    private void saveToCSV(String text) {
        try {
            File f = new File("cachorros.csv");
            if (f.exists()) {
                FileWriter writer = new FileWriter("cachorros.csv", true);
                writer.append(text);
                writer.close();
                
            } else {
                FileWriter writer = new FileWriter("cachorros.csv");

                writer.append("Nome;Raca;Sexo;Idade;Cor;\n");
                writer.append(text);
                writer.flush();
                writer.close();
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    


